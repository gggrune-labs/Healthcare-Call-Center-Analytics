
/* 1. call_agents*/

DROP TABLE IF EXISTS calls;
DROP TABLE IF EXISTS call_agents;
DROP TABLE IF EXISTS policy_holders;

CREATE TABLE call_agents (
    agent_id        SERIAL PRIMARY KEY,
    agent_name      VARCHAR(60)  NOT NULL,
    department      VARCHAR(30)  NOT NULL,   -- 'Claims', 'Benefits', 'Enrollment'
    hire_date       DATE         NOT NULL,
    skill_level     SMALLINT     NOT NULL     -- 1 (junior) → 5 (senior)
        CHECK (skill_level BETWEEN 1 AND 5)
);

-- 2. policy_holders
CREATE TABLE policy_holders (
    member_id       SERIAL PRIMARY KEY,
    member_name     VARCHAR(80)  NOT NULL,
    plan_type       VARCHAR(20)  NOT NULL,   -- 'HMO', 'PPO', 'EPO', 'HDHP'
    enrollment_date DATE         NOT NULL,
    state           CHAR(2)      NOT NULL,
    date_of_birth   DATE         NOT NULL
);


-- 3. calls
CREATE TABLE calls (
    call_id             SERIAL PRIMARY KEY,
    member_id           INT          NOT NULL REFERENCES policy_holders(member_id),
    agent_id            INT          NOT NULL REFERENCES call_agents(agent_id),
    call_start          TIMESTAMP    NOT NULL,
    call_end            TIMESTAMP,                -- NULL = abandoned / dropped call
    call_category       VARCHAR(30)  NOT NULL,    -- 'Claims', 'Benefits', 'Billing',
                                                  -- 'Enrollment', 'Pharmacy', 'Prior Auth'
    resolution          VARCHAR(20),              -- 'Resolved', 'Escalated', 'Callback',
                                                  -- 'Transferred', NULL (abandoned)
    satisfaction_score  SMALLINT                   -- 1-5 scale; NULL = no survey
        CHECK (satisfaction_score BETWEEN 1 AND 5)
);

-- Index recommendations (referenced in query optimization notes)
CREATE INDEX idx_calls_member_id      ON calls (member_id);
CREATE INDEX idx_calls_agent_id       ON calls (agent_id);
CREATE INDEX idx_calls_call_start     ON calls (call_start);
CREATE INDEX idx_calls_category       ON calls (call_category);
CREATE INDEX idx_calls_member_start   ON calls (member_id, call_start);

